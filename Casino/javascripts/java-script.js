
$( document ).ready(function(){
	  $( ".fortuna" ).mouseover(function(){ // задаем функцию при нажатиии на элемент с классом start
	    $( ".fortuna5" ).animate({
	      rotate: "2000deg",
	    }, {
	      duration: 20000, // продолжительность анимации
	      easing: "linear", // скорость анимации
        animate: "infinite",
	      complete: function(){ // callback
	        $(".status").text("Анимация завершена");
	      },
	      queue: false // не ставим в очередь
	    });
	  });
	});

